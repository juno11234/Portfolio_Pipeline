/**
 * 콘텐츠 문장에 허용하는 아주 작은 마크업.
 *
 * 네 가지뿐이다. 색이 곧 의미다 — 두 문서를 대조해 색이 무슨 일을 하는지 세어보고 정했다.
 *   `**굵게**`  — 잉크 볼드(#17181a). 문제·핵심 용어를 짚는다.        (뱀서 26 · Bond 51)
 *   `==해법==`  — 파란 볼드(#1d4ed8). **내가 고른 해법·기제**를 짚는다.  (뱀서 6 · Bond 15)
 *   `!!실패!!`  — 빨간 볼드(#b91c1c). 실패·버그가 일어난 지점.          (뱀서 0 · Bond 1)
 *   `` `코드` `` — 모노 파랑. 코드에 실재하는 이름.                    (뱀서 · Bond 55)
 *
 * **파란 볼드가 이 포트폴리오에서 가장 값진 표시다.** 가이드가 말하는 "'그냥'은 금지어,
 * 왜 그걸 골랐나"를 색으로 드러낸다. 잉크(문제)와 파랑(해법)이 한 문장에서 갈린다.
 * 예: "높은 결합도(**잉크**)를 ==인터페이스 + DI==로 뒤집었다".
 *
 * 처음엔 파란 볼드를 `<code>` 로 바꾸자고 했다가 접었다 — 뱀서라이크 4곳만 보고 내린
 * 판단이었고, Bond 15곳이 반증했다. 색은 클래스 이름 표시가 아니라 '해법' 표시였다.
 *
 * 왜 이것만인가 — 마크다운 전체를 열면 콘텐츠 파일이 반쯤 마크다운이 되어
 * 스키마가 검사할 수 없는 구조(표·목록·제목)가 문장 안으로 들어온다.
 * 구조는 블록이 갖고, 문장은 강조만 한다.
 *
 * `==` · `!!` 를 고른 근거: 두 문서의 산문(코드블록 제외)에 이 두 기호가 한 번도
 * 나오지 않는다. 코드의 `==` 는 전부 `<pre>` 안이라 rich() 를 거치지 않는다.
 * `==` 는 마크다운 하이라이트 관용(==text==)과도 같아 의미가 멀지 않다.
 */

/**
 * &, <, > 를 막는다. `set:html` 로 넣기 전에 반드시 통과시킨다.
 * 순서가 반대면(마크업을 먼저 열면) 콘텐츠로 태그를 주입할 수 있게 된다.
 *
 * rich() 말고도 `set:html` 을 쓰는 곳이 있어 내보낸다.
 * 줄을 `<br>` 로 잇는 자리(ClassBox 의 필드·메서드 칸 등)는 강조 마크업은 필요 없고
 * 이스케이프만 필요하다. C# 제네릭(`Dictionary<int, T>`)이 태그로 새는 것을 막는 게 요점이다.
 */
export function escapeHtml(text: string): string {
  return text.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

/**
 * 문장을 HTML 조각으로 바꾼다. `set:html` 로 넣어야 한다.
 *
 * 순서가 중요하다. 이스케이프를 가장 먼저 해 콘텐츠가 태그를 주입하지 못하게 막는다.
 * 그 다음 볼드류를 열고, 코드는 맨 뒤에 연다 — 코드 안 텍스트에는 볼드를 열지 않기 위해서다.
 * `**` 를 `==`·`!!` 보다 먼저 처리해 별표가 다른 기호와 얽히지 않게 한다.
 *
 * @param text 콘텐츠 파일의 원문
 */
export function rich(text: string): string {
  return escapeHtml(text)
    .replace(/\*\*([^*]+)\*\*/g, '<b>$1</b>')
    .replace(/==([^=]+)==/g, '<b style="color:#1d4ed8">$1</b>')
    .replace(/!!([^!]+)!!/g, '<b style="color:#b91c1c">$1</b>')
    .replace(/`([^`]+)`/g, '<code>$1</code>');
}
