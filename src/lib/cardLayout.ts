/**
 * 카드 묶음을 가로(격자) vs 세로(스택) 중 무엇으로 낼지 정하는 **렌더러 규칙**.
 *
 * CLAUDE.md 원칙 그대로 — 콘텐츠 파일은 레이아웃(CSS)을 들지 않는다. 여백처럼 이것도 렌더러가
 * 규칙으로 정한다. 짧은 설명은 좁은 3열 칸에 잘 들어가지만, 긴 설명(대략 2줄 넘게)은 좁은 칸에서
 * 눌려 읽기 나쁘다. 그래서 **카드 중 하나라도 본문이 기준보다 길면 전폭 세로**, 아니면 가로 격자다.
 *
 * 손으로 `variant: stack` 을 붙이던 것을 이 규칙으로 대체했다(사용자 요청: 짧은 곳 가로 · 긴 곳 세로).
 * 자동 판정이 어긋나는 예외는 `variant: stack`(강제 세로) 으로 덮어쓸 수 있다.
 */

/**
 * 3열(~230px) 카드에서 한국어 본문이 대략 2줄을 넘기는 길이. 넘으면 세로가 낫다.
 * (좁은 칸 한 줄 ≈ 25~30자 → 2줄 ≈ 55~60자)
 */
export const LONG_CARD_BODY = 60;

/** 카드 묶음을 세로로 내야 하는지 — 본문이 하나라도 기준보다 길면 참(한 장이 길면 격자에서 그 칸만 튄다). */
export function cardsAreLong(cards: ReadonlyArray<{ body?: string | undefined }>): boolean {
  return cards.some((c) => (c.body ?? '').length > LONG_CARD_BODY);
}
